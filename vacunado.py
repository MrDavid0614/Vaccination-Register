class Vacunado:
    def __init__(self, cedula, nombre, apellido, telefono, fechaNacimiento, dosis, provincia, fechaRegistro) -> None:
        self.cedula = cedula
        self.nombre = nombre
        self.apellido = apellido
        self.telefono = telefono
        self.fechaNacimiento = fechaNacimiento
        self.dosis = dosis
        self.provincia = provincia
        self.fechaRegistro = fechaRegistro